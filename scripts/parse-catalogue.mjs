// Parses the prototype's rituals-catalogue.md into seed SQL for the `rituals` table.
//   node scripts/parse-catalogue.mjs > supabase/seed.sql
import { readFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const SRC = resolve(
  __dirname,
  '../claude-design-artefacts/Small good moments prototype/rituals-catalogue.md',
);

const md = readFileSync(SRC, 'utf8');
const lines = md.split('\n');

const PROSE = {
  Essence: 'essence',
  Meaning: 'meaning',
  'How to': 'how_to',
  When: 'when_context',
  Feeling: 'feeling',
  'Cultural note': 'cultural_note',
  Caution: 'caution',
};

const entries = [];
let i = 0;
let order = 0;
while (i < lines.length) {
  const line = lines[i];
  // A ritual is a "### Heading" immediately (within a couple of lines) followed by a ```yaml block.
  if (line.startsWith('### ')) {
    let j = i + 1;
    while (j < lines.length && lines[j].trim() === '') j++;
    if (lines[j] && lines[j].trim().startsWith('```yaml')) {
      const entry = {};
      // parse yaml
      let k = j + 1;
      while (k < lines.length && !lines[k].trim().startsWith('```')) {
        const m = lines[k].match(/^(\w+):\s*(.*)$/);
        if (m) {
          let val = m[2].trim();
          if (val.startsWith('"') && val.endsWith('"')) val = val.slice(1, -1);
          entry[m[1]] = val;
        }
        k++;
      }
      // parse prose fields until next heading / hr
      let p = k + 1;
      while (p < lines.length && !lines[p].startsWith('### ') && !lines[p].startsWith('## ')) {
        const pm = lines[p].match(/^\*\*([^:*]+):\*\*\s*(.*)$/);
        if (pm && PROSE[pm[1]]) entry[PROSE[pm[1]]] = pm[2].trim();
        p++;
      }
      entry.sort_order = order++;
      entries.push(entry);
      i = p;
      continue;
    }
  }
  i++;
}

const q = (v) => (v == null ? 'NULL' : `'${String(v).replace(/'/g, "''")}'`);
const qArr = (steps) =>
  'ARRAY[' + steps.map((s) => q(s.trim())).join(', ') + ']::text[]';
const qBool = (v) => (String(v) === 'true' ? 'true' : 'false');
const qInt = (v) => {
  const n = parseInt(v, 10);
  return Number.isFinite(n) ? String(n) : '0';
};
const nullable = (v) => (v && v.length ? q(v) : 'NULL');

const cols = [
  'id', 'name', 'language', 'region', 'pronunciation', 'cluster', 'type',
  'promptable', 'solo_or_social', 'time_of_day', 'season', 'duration_minutes',
  'essence', 'meaning', 'how_to', 'when_context', 'feeling', 'cultural_note',
  'lomas_path', 'caution', 'sort_order',
];

const rows = entries.map((e) => {
  const how = (e.how_to || '').split('|').map((s) => s.trim()).filter(Boolean);
  return (
    '  (' +
    [
      q(e.id), q(e.name), q(e.language), q(e.region), q(e.pronunciation),
      q(e.cluster), q(e.type), qBool(e.promptable), q(e.solo_or_social),
      q(e.time_of_day), q(e.season), qInt(e.duration_minutes),
      q(e.essence), q(e.meaning), qArr(how), q(e.when_context), q(e.feeling),
      q(e.cultural_note || ''), nullable(e.lomas_path), nullable(e.caution),
      String(e.sort_order),
    ].join(', ') +
    ')'
  );
});

const sql = `-- Generated by scripts/parse-catalogue.mjs — do not edit by hand.
-- ${entries.length} rituals from the Small Good Moments catalogue.
insert into public.rituals (${cols.join(', ')})
values
${rows.join(',\n')}
on conflict (id) do update set
  name = excluded.name,
  language = excluded.language,
  region = excluded.region,
  pronunciation = excluded.pronunciation,
  cluster = excluded.cluster,
  type = excluded.type,
  promptable = excluded.promptable,
  solo_or_social = excluded.solo_or_social,
  time_of_day = excluded.time_of_day,
  season = excluded.season,
  duration_minutes = excluded.duration_minutes,
  essence = excluded.essence,
  meaning = excluded.meaning,
  how_to = excluded.how_to,
  when_context = excluded.when_context,
  feeling = excluded.feeling,
  cultural_note = excluded.cultural_note,
  lomas_path = excluded.lomas_path,
  caution = excluded.caution,
  sort_order = excluded.sort_order;
`;

process.stdout.write(sql);
process.stderr.write(`Parsed ${entries.length} rituals\n`);
